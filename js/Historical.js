function padTo2Digits(num) {
  return num.toString().padStart(2, '0');//padStart(มี 2 ตำแหน่ง, เริ่มด้วย '0') Ex. '02'
}
function formatDate(date = new Date()) {
  return [
    date.getFullYear(),
    padTo2Digits(date.getMonth() + 1),
    padTo2Digits(date.getDate()),
  ].join('-');
}